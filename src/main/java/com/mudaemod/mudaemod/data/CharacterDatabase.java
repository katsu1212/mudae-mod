    public static Character rollRandom() {
        return rollRandomExcluding(Set.of());
    }

    public static Character rollRandomExcluding(Set<Integer> excludedIds) {
        List<Character> pool = new ArrayList<>();
        for (Character c : ALL) {
            if (!excludedIds.contains(c.getId()))
                for (int w = 0; w < rankWeight(c.getRank()); w++) pool.add(c);
        }
        if (pool.isEmpty()) return null;
        return pool.get(new Random().nextInt(pool.size()));
    }

    public static Character getById(int id) {
        for (Character c : ALL) if (c.getId() == id) return c;
        return null;
    }

    public static int getRank(int id) {
        Character c = getById(id);
        return c != null ? c.getRank() : 1;
    }

    /** Rank 1 = comun (peso 50), Rank 5 = legendario (peso 1) */
    private static int rankWeight(int rank) {
        return switch (rank) {
            case 1 -> 50;
            case 2 -> 20;
            case 3 -> 8;
            case 4 -> 3;
            default -> 1;
        };
    }
